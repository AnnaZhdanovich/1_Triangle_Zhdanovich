package by.zhdanovich.triangle.exception;

public class WrongCoordinateException extends Exception {

	public WrongCoordinateException() {
		super();
		
	}

	
	public WrongCoordinateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public WrongCoordinateException(String arg0) {
		super(arg0);
		
	}

	public WrongCoordinateException(Throwable arg0) {
		super(arg0);
		
	}
	

}
